#!/usr/bin/env python3

def printAny(any):
    print(any)

def printSentence(name, age):
    print("Hello I'm {}, and I am {} years old.".format(name, age))

def printCalc(nb1, nb2, nb3):
    res = nb1 + nb2 - nb3
    print(res)

def printCalc2(nb1, nb2, nb3):
    res = nb1 * nb2 / nb3
    print(res)

def printTab(str1, str2, str3):
    tab = [str1, str2, str3]
    print(tab)

